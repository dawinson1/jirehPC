        <h4 style="font-family:verdana;">Planteamiento del problema:</h4>
                <br>
        <p style="font-family:verdana,helvetica;">
El almacén JIREH PC se encuentra ubicado en la carrera 89 # 31- 61 en el barrio Belén Las Violetas, está conformado por 5 empleados, capacitados para el mantenimiento, reparación y venta de equipos de cómputo y móviles.

JIREH PC actualmente ofrece los servicios de venta a domicilio a través de una línea de WhatsApp lo que genera retrasos en el tiempo de respuesta al cliente y la mayoría de veces pérdida de información del mismo, por otro lado, el almacén no cuenta con un registro o historial de los pedidos realizados por dicha línea.
Nos comprometeremos a abordar estos procesos, de manera que el usuario cliente tenga la posibilidad de verificar que productos hay en existencia en el catálogo web para posteriormente pedirlos a domicilio vía telefónica. 
</p>
<br>
<br>
<h4 style="font-family:verdana;">Justificación:</h4>
                <br>
        <p style="font-family:verdana,helvetica;">Partiendo de la necesidad del almacén JIREH PC, se propone desarrollar un aplicativo web que gestione el catálogo en línea para visualizar productos (celulares y artículos de cómputo) y que además genere registro de pedidos e historial de estos.
Nosotros como aprendices tendremos la oportunidad de desarrollar un sistema de información el cual nos ayudará a mejorar los conocimientos adquiridos durante la etapa lectiva, a trabajar en equipo y a ser profesionales competentes en el medio.

</p>
   
<br>
<br>
<h4 style="font-family:verdana;">Objetivos del proyecto:</h4><br>
<h4 style="font-family:verdana;">General:</h4>
<p style="font-family:verdana;">Desarrollar un aplicativo web que gestione los procesos de pedidos y control de existencias de artículos de cómputo y celulares que la empresa JIREH PC ofrece a sus clientes.</p>

<h4 style="font-family:verdana;">Específicos:</h4>
<li style="font-family:verdana;">Administrar pedidos teniendo en cuenta las existencias de productos que la empresa brinda a sus clientes.</li>
<li style="font-family:verdana;">Gestionar un catálogo de productos y servicios que permita al cliente obtener información 
        de los servicios que brinda la empresa y el costo de cada uno.
</li>
<br>
<br>
<h4 style="font-family:verdana;">Alcance:</h4>
                <br>
        <p style="font-family:verdana,helvetica;">
        El aplicativo web entregará a JIREH PC los módulos de catálogo para que los usuarios puedan visualizar los productos y el módulo de pedidos donde el administrador ingresará los pedidos que soliciten los clientes con posibilidad de llevarlos a domicilio.
</p>
<br><br>
<h4 style="font-family:verdana;">Módulos:</h4>
<h4 style="font-family:verdana;">Módulo de Empleado:</h4>
<p style="font-family:verdana;">El módulo de  empleado se encarga de gestionar la información de todos los empleados del almacén Jireh pc.
Los roles que tendrán acceso y permisos en este módulo serán Administrador y Técnico.</p>
<br>
<h4 style="font-family:verdana;">Módulo de Pedidos:</h4>
<p style="font-family:verdana;">El módulo de  pedido se encarga de gestionar las ventas o pedidos de productos que se realicen en Jireh Pc y permite asignar una fecha de entrega y un técnico.
Los roles que tendrán acceso y permisos en este módulo serán Administrador y Técnico.</p>
